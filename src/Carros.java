public class Carros {
    String matricula;
    int anio;
    String modelo;
    String color;
    Carros(){
        matricula = "3547 NBX" ;
        anio = 2022;
        modelo = "Toyota";
        color = "Blanco";
    }

    public String getMatricula() {
        return matricula;
    }

    public void setMatricula(String matricula) {
        this.matricula = matricula;
    }

    public int getAnio() {
        return anio;
    }

    public void setAnio(int anio) {
        this.anio = anio;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
